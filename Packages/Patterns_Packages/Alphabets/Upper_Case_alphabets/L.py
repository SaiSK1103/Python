def for_L():
        """ Upper case Alphabet letter 'L' pattern using Python Python for loop"""

        for row in range(6):
                for col in range(4):
                        if col==0 or row==5:
                                print('*', end = ' ')
                        else:
                                print(' ', end = ' ')
                print()


