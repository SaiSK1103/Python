def for_leftange_triangle():           

        """Shape of 'leftangle Triangle' using Python for loop"""

        for row in range(1,6):
                print(' '*(5-row),'*'*row)

def while_leftange_triangle():           

        """Shape of 'leftangle Triangle' using Python while loop"""
        row = 1
        while row<6:
                print(' '*(5-row),'*'*row)
                row += 1

