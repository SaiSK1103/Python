
def for_rightangle_triangle():

        """Shape of 'Rightangle Triangle' using Python for loop"""

        for row in range(1,6):
                print('* '*row)


def while_rightangle_triangle():

        """Shape of 'Rightangle Triangle' using Python while loop"""
        row = 1
        while row<6:
                print('* '*row)
                row += 1

