
def for_tltriangle():
        """Shape of 'Top_Leftangle Triangle' """

        for row in range(6,0,-1):
                print(' '*(6-row),'*'*row)


def while_tltriangle():
        """Shape of 'Top_Leftangle Triangle' using Python while loop"""
        row = 6
        while row>0:
                print(' '*(6-row),'*'*row)
                row -= 1

