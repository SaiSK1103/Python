
def for_trtriangle():
        """Shape of 'Top_Rightangle Triangle' using Python for loop """

        for row in range(6,0,-1):
                print('* '*row)



def while_trtriangle():
        """Shape of 'Top_Rightangle Triangle' using Python while loop """
        row = 6
        while row>0:
                print('* '*row)
                row -= 1

